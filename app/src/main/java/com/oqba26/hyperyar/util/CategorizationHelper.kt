    fun suggestCategory(productName: String): String {
        if (productName.isBlank()) return "عمومی"
        
        val normalizedName = productName.normalizePersian()
        
        for ((category, keywords) in categoryKeywords) {
            if (keywords.any { keyword -> 
                normalizedName.contains(keyword.normalizePersian()) 
            }) {
                return category
            }
        }
        
        return "عمومی"
    }

    fun suggestUnit(productName: String): String {
        if (productName.isBlank()) return "عدد"
        
        val normalizedName = productName.normalizePersian()
        
        for ((unit, keywords) in unitKeywords) {
            if (keywords.any { keyword -> 
                normalizedName.contains(keyword.normalizePersian()) 
            }) {
                return unit
            }
        }
        
        return "عدد"
    }

    private fun String.normalizePersian(): String {
        return this.lowercase()
            .replace('ي', 'ی')
            .replace('ك', 'ک')
            .replace(" ", "")
            .replace("\u200c", "") // half-space
    }
